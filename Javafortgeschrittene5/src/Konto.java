
public class Konto {
	
	
	private String isbn;
	private double saldo;
	
	
	
	
	
	
	
	
	public Konto() {
		
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public double getSaldo() {
		return saldo;
	}
	public void setSaldo(double saldo) {
		this.saldo = saldo;
	}
	
	
	
	
	

}
